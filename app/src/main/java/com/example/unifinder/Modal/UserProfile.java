package com.example.unifinder.Modal;

public class UserProfile {

    String name,username,email,mobile_no,alternate_mobile_no;

    public UserProfile(){
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile_no() {
        return mobile_no;
    }

    public void setMobile_no(String mobile_no) {
        this.mobile_no = mobile_no;
    }

    public String getAlternate_mobile_no() {
        return alternate_mobile_no;
    }

    public void setAlternate_mobile_no(String alternate_mobile_no) {
        this.alternate_mobile_no = alternate_mobile_no;
    }
}
